				<p>The demands of humanity upon the earth’s ecosystems, which have supplied industry with resources and simultaneously absorbed the waste from the manufacturing process, have increased at
an alarming rate. As the world’s population grows alongside consumer aspirations for a high standard of living, the consumption of goods will similarly increase and put further pressure on the resources readily available to the manufacturing industry. A radical change in our approach to production, consumption, processing, storage, recycling and disposal of biological resource is crucial. Yet while a bio-based economy is vital and perfectly feasible, significant investments are required to support a sustainable alternative economy.</p>

<p>It is a huge undertaking to achieve sustainable production and resource efficiency, through the creation of goods and services which use processes and systems that conserve resources, maximise recovery, reuse, and recycling of materials and minimise waste is a huge undertaking. However, it must be focused upon now and addressed in order to protect our existing resources and to reap the potentially huge economic benefits a bio economy based manufacturing industry would offer.</p>

<p>How can such a large issue be tackled? The key is accepting that most problems are unsolvable which enables a more lateral thought process and to view the bigger picture. Often there are no</p>

				<p>preconceived ideas or a solution would already have presented itself. It is in fact the smallest of changes that regularly yield the biggest results. From reducing energy intensity and substituting existing materials with those that are more secure or have a lower environmental impact, to using less of the material and then the same material over multiple product lives, changes can be made.
				</p>
				<p>
				Our Sustainable Processing and Printable Electronics Centres are working in tandem to address the issues and we are making great progress. Deriving energy from waste; fully utilising feedstocks; developing lightweight materials and new composites we are designing and manufacturing with sustainability in the forefront of our mind.
				</p>
				
				<h4 class="pinktext">Barriers to resource efficiency:</h4>
				<ul>
					<li><strong class="pinktext">Investment and outlay</strong> – the technology and its outputs are not always more cost-effective</li>
					<li><strong class="pinktext">Infrastructure</strong> – a closed loop approach is dependent upon the buy-in of the wider industry at all levels in the value chain</li>
				</ul>
				
				<h4 class="bluetext">CPI breaks these barriers down by:</h4>
				<ul>
<li><strong class="bluetext">Exploiting the low carbon market</strong> through its proven low carbon technologies</li>
<li><strong class="bluetext">Harnessing flexible and adaptive manufacturing</strong> processes and systems to run multiple inputs through one system to generate different outputs</li>
<li><strong class="bluetext">Maximising efficiency</strong> through continuous processing designed to optimise scalability and outputs</li>
				</ul>

				
