					Peratech Limited
				</p>
				<img src="http://placehold.it/1000x400&text=[img]" class="full-image" />
			</div>
			<div class="eight columns">
				<p>Peratech Limited was established in 1996 to exploit Quantum Tunnelling Composite (QTCTM Material), which is a new class of electrically conductive material developed to advance the capability of switching and sensing systems.</p>
				
				<p>QTC materials give enormous flexibility in the design, shape, thickness and style of a switch or touch sensor and can be made in a range of forms from traditional switch replacements, through textile and screen printed switches to innovative, pressure-sensitive QTC Touchscreens that allow for three dimensions of input.</p>
				
				<p>Potential uses for QTC are numerous, ranging from textiles to touch screens. The technology has already been applied in clothing, where controls for iPods and similar devices have been successfully integrated into jackets, which can be washed or dry cleaned, crumpled and stretched.</p>

				<p>Peratech approached CPI with a view to broadening its ink technology into a more widely used set of printing processes. CPI brought its experience of ink formulation, print processing and testing expertise to help Peratech develop these new products.</p>
<p>David Lussey, CEO of Peratech said:</p>
<p class="bluetext">“The next innovation in electronics is being able to print complete circuit assemblies as this dramatically reduces unit costs. Both active and passive components are being printed onto paper, textiles and plastics using flexographic printing processes. The development project with CPI is designed to create QTC ink formulations that can be used in this and similar printing processes so that QTC pressure sensors and switches can be incorporated into these next generation printed electronic circuits.”</p>
