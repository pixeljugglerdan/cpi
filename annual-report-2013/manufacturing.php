				<h2>Manufacturing Process in action</h2>
			</div>
		</div>
		
		
		<div class="row">
			<div class="four columns">
				<p class="intro">
					Sanofi Aventis
				</p>
				<img src="http://placehold.it/1000x400&text=[img]" class="full-image" />
			</div>
			<div class="eight columns">
				<p>Sanofi Aventis aims to become a diversified global healthcare leader, and to this end utilised the facilities at CPI to demonstrate the potential of Corning Advanced-Flow reactors for multiphase hydrogenations in high volume manufacturing.</p>
				
				<p>In the pharmaceutical and fine chemicals industries, most multiphase hydrogenation reactions are conducted in large batch or semi-batch reactors, in which the catalyst is suspended in liquid and continuously stirred. Batch reactors are often susceptible to lack of temperature control, which can result in hot spots on the catalyst’s surface. These hotspots lead to the formation of several by-products or intermediaries, which can result in decomposition. In recent years there has been increasing interest in the testing of micro reactors at laboratory scale for catalytic hydrogenations as, due to their small transverse dimensions, they have exceptionally high surface to volume ratios and therefore exhibit enhanced heat and mass transfer rates. Typically, the micro reactors are producing from 0.1 to 10 grams per hour, so are very well suited for screening or small scale production. A natural progression was to scale up</p>
				
				<p>this process for industrial production to several hundred tons per year, which requires production rates of at least one hundred times higher.</p>

<p>CPI, Sanofi-Aventis and Corning believed the implementation of slurry hydrogenation in continuous flow reactors would effectively solve this issue. The method was tested at CPI’s state-of-the-art flow reaction facility using Corning’s Advanced-Flow glass reactor, an assembly of glass fluidic modules connected together through appropriate piping. CPI successfully operated the reactor and the conversion, selectivity, impurity profile and the catalyst content were studied by the highly skilled team. After over 300 hours of operation, results concluded that over 98 percent conversion could be achieved with the continuous flow reactor for starting material concentrations from 30 wt percent to 45 wt percent. The catalyst content proved to have very little impact in a wide range of concentrations, opening the door to potential savings versus the batch reactor processing. These results successfully prove the high potential of Corning Advanced-Flow reactors for slurry-based hydrogenation at an industrially important scale.</p>
