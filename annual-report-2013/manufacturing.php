		<div class="row">
			<div class="eight columns">
				<p>The growing economic power of Asia; the Government focus upon innovation and research and development; the birth of relatively new industries such as renewable energy and photonics and an increased demand from end users with more disposable income are all shaping the direction of the industry. However, the overarching challenge is clear – in order to compete globally on a level manufacturing plane, the UK must create more adaptable and cost efficient processes to advance
its manufacturing systems and create new business models to support them.</p>

<p>Only by evaluating and in some cases deconstructing the manufacturing process can it truly become more responsive to changing market demands. There has never been a greater need for processes to be bespoke and entirely flexible to reduce costs and make the entire process sector leaner and more efficient. The evolution of process engineering is now focused upon developing techniques concurrently and creating systems that allow for reconfigurable manufacturing.</p>

<p>While the potential for techniques such as additive manufacture may be well documented and publicised, the use of such cost saving, efficient processes are not yet mainstream. However, digital layering processes dispense with exorbitant tooling costs and minimise waste. These processes only uses the material needed to make a part whereas traditional subtractive process can waste up to 95 per cent of the raw material used to create a component. Consequently additive processes require far less raw material. Furthermore, the digital layering process removes traditional product design constraints empowering designers to develop lightweight, complicated geometric products previously unimaginable.</p>

				<p>If all industries, especially those in the high value manufacturing sector, could understand the need and inherent benefits of developing new processes then the vital link between designer and developer could also be fully restored bridging a key part of the gap in the value chain and strengthening the UK’s position in the manufacturing field.</p>
						

								
				<h4 class="pinktext">Barriers to enhanced flexibility and high value manufacturing:</h4>
<ul>						
<li><strong class="pinktext">Costs</strong> – manufacturing costs remain high and significant investment in the economy is needed</li>
<li><strong class="pinktext">Materials shortage</strong> – resources are not finite and sustainable alternatives are needed</li>
<li><strong class="pinktext">Skills shortage</strong> – a greater feed of new talent in the fields of science, maths, engineering and technology</li>
<li><strong class="pinktext">Demonstration</strong> – the inability to showcase new products and processes in a commercial setting results in products failing at the prototype stage</li>
<li><strong class="pinktext">Weakened supply chains</strong> - SMEs continue to compete with the high volume of manufactured imports. New business models are required to harness innovation</li>		
</ul>						
						
				
