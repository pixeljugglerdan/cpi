				<p>The threat posed by emerging economies such as the BRIC and N11 territories are real and have been for some time now. This threat coupled with the advances made by the German, Japanese and US markets particularly in terms of high value manufacturing is gradually diminishing the impact the UK has in industry. The challenge to develop new supply chain and production networks in the UK has therefore never been more critical, as the emergence and establishment of global value chains continues to minimise value capture and has a negative impact upon the UK economy. Moreover our SMEs face huge barriers in accessing global value chains and as such are unable to compete with their global counterparts. However, the challenge is not about simplifying access to global production networks. It is much more complicated, as in fact the UK must continue to develop its own integrated value networks capable of sustaining the economy and realising the superior value systems of old.</p>

<p>While it may seem too simple, the best solution for effectively managing fragmented value chains is to develop and build new business models that bridge any gaps and link all stages in the manufacturing process together as one whole. The production of intermediate goods such as components and parts production may be the larger sum of the overall fragmentation the UK is now facing, but the breakdown of our value chain has also included the accompanying knowledge intensive services, such as R&D, inventory management, quality control, and other professional and technical services. If the UK is to truly regain its eminence in the manufacturing industry then new systems centred around cross disciplinary skill sets and new value propositions are vital.</p>
								
