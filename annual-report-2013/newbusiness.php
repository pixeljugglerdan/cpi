				<h2>The Northern Way Printed Electronic Supply Chain Programme</h2>
			</div>
		</div>
		
		
		<div class="row">
			<div class="four columns">
				<p>The programme is a unique initiative, bringing together the cities and regions of the North of England to
work together to improve the sustainable economic development of the North. The Northern Way works with local authorities, universities, the private sector and other partners to secure a strong coalition in support of this goal.</p>

				<p>The Northern Way’s ‘Building the Supply Chain in Printed Electronics’ programme is targeted at providing the funding and collaboration infrastructure within which participating organisations can explore and ultimately exploit the opportunity within the Printed Electronics market.</p>
			</div>
			<div class="four columns">
				
				<p>The competition was broken down into a series of work packages to stimulate the growth of a supply chain and ultimately develop working demonstrators of what the technology could achieve. The competition involved twenty organisations across eight projects and directly resulted in at least eight patents being filed.</p>
				
				<p>Work packages one and two were focused on the scale up of molecules to one kilo and beyond for repeatable batches of Organic Semiconductors (OSCs), and on the development of ink formulation for printing. The aim was to transfer this gained knowledge to the chemical companies, who, in turn, subsequently developed a range of OSCs, increased links and increased the understanding of the technology’s potential, workings and benefits.</p>

			</div>
		
			<div class="four columns">
								
<p>Packages four and five were focused on the development process, working under the principle that simpler structures give superior performance and reduced costs. Challenges in this area were to gain knowledge of what is required when processing material to develop transistor performance great enough to drive Liquid Crystal Displays (LCDs) and to continually improve the quality and applications for Organic Light-Emitting Diode (OLED).</p>

<p>The final work packages centred around the Demonstrators competition, in which eight demonstrator projects were developed and working prototypes produced.</p>
